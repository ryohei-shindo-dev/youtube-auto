def generate_voice_for_scenes(
    scenes: list,
    output_dir: pathlib.Path,
) -> list:
    """
    ElevenLabs API で各シーンのナレーション音声を生成する。

    Args:
        scenes: script_gen が返した scenes リスト
        output_dir: 音声ファイルの保存先ディレクトリ

    Returns:
        scenes に "audio_path" と "actual_duration_sec" を追加したリスト。
        失敗したシーンの audio_path は None になる（処理は継続）。
    """
    api_key = os.getenv("ELEVENLABS_API_KEY", "")
    voice_id = os.getenv("ELEVENLABS_VOICE_ID", "")
    if not api_key or not voice_id:
        print("  [エラー] ELEVENLABS_API_KEY または ELEVENLABS_VOICE_ID が設定されていません。")
        for scene in scenes:
            scene["audio_path"] = None
            scene["actual_duration_sec"] = 0
        return scenes

    output_dir.mkdir(parents=True, exist_ok=True)

    prev_text = ""
    prev_request_ids: list[str] = []

    for i, scene in enumerate(scenes):
        idx = i + 1
        output_path = output_dir / f"audio_{idx:02d}.mp3"
        raw_text = scene.get("text", "")
        role = scene.get("role", "unknown")

        # TTS用にテキストを整形
        text = _format_for_tts(raw_text)

        print(f"  シーン{idx}（{role}）の音声を生成中...")
        # closingには前シーンのコンテキストを渡してノイズ軽減
        success, request_id = _text_to_speech(
            api_key, voice_id, text, output_path, role=role,
            previous_text=prev_text if role == "closing" else "",
            previous_request_ids=prev_request_ids if role == "closing" else None,
        )

        if success:
            # 冒頭の無音をトリミング（ElevenLabsの音声は先頭に無音が入ることがある）
            trimmed = _trim_leading_silence(output_path)
            if trimmed:
                print(f"    冒頭無音をトリミングしました")
            duration = _get_audio_duration(output_path)
            scene["audio_path"] = str(output_path)
            scene["actual_duration_sec"] = duration
            size_kb = output_path.stat().st_size // 1024
            print(f"    保存完了: {output_path.name}（{duration:.1f}秒 / {size_kb}KB）")
            # 次シーンのために保存
            prev_text = text
            prev_request_ids = [request_id] if request_id else []
        else:
            scene["audio_path"] = None
            scene["actual_duration_sec"] = 0
            print(f"    [エラー] シーン{idx}の音声生成に失敗しました。")

    total_sec = sum(s.get("actual_duration_sec", 0) for s in scenes)
    success_count = sum(1 for s in scenes if s.get("audio_path"))
    print(f"  音声生成完了（{success_count}/{len(scenes)}シーン成功 / 合計{total_sec:.1f}秒）")

    return scenes


def _format_for_tts(text: str) -> str:
    """
    ElevenLabs 用にテキストを整形する。

    ルール:
      0. 読み間違えやすい漢字をひらがなに変換
      1. 改行を除去（1つの連続テキストにする）
      2. 「%」→「パーセント」に変換（記号を正しく読ませる）
      3. 「&」で囲まれていない単独の「&」→「アンド」（S&P500等は除く）
      4. 句読点「。」の後に半角スペースを挿入（自然なポーズ）
      5. 「！」「？」の後に半角スペースを挿入（感情の間）
      6. 連続する空白を1つに正規化
    """
    # 読み間違えやすい漢字の読み替え辞書
    # ElevenLabsが間違える漢字→正しい読みのひらがなに変換
    for wrong, correct in _READING_FIXES:
        text = text.replace(wrong, correct)

    # 改行を除去
    text = text.replace("\n", "")

    # 視覚用の記号は読み上げ対象から外す
    text = re.sub(r"(?:…|⋯)+", " ", text)
    text = re.sub(r"(?:\.{3,}|・{3,})", " ", text)
    text = re.sub(r"[※→←↑↓]", " ", text)
    text = re.sub(r"[【】『』]", " ", text)
    text = re.sub(r"(?:ーー+|――+|—+)", " ", text)

    # 「%」→「パーセント」
    text = text.replace("%", "パーセント")

    # 単独の「&」→「アンド」（ただし S&P のような英字に挟まれた & は除く）
    text = re.sub(r"(?<![A-Za-z])&(?![A-Za-z])", "アンド", text)

    # 「でも」「だから」「だけど」の後に間を入れる（断言前のポーズ）
    text = re.sub(r"(でも、|だから、|だけど、)", r"\1 ", text)

    # 句読点・感嘆符の後にスペースを挿入（ポーズ用）
    text = re.sub(r"([。！？])", r"\1 ", text)

    # 連続空白を1つに
    text = re.sub(r"\s+", " ", text).strip()

    return text


def _text_to_speech(
    api_key: str,
    voice_id: str,
    text: str,
    output_path: pathlib.Path,
    role: str = "",
    previous_text: str = "",
    previous_request_ids: list | None = None,
) -> tuple[bool, str]:
    """ElevenLabs API で1シーン分の音声を生成する。

    Returns:
        (成功フラグ, request_id) のタプル。失敗時は (False, "")。
        request_id は次シーンの previous_request_ids に渡す。
    """
    url = ELEVENLABS_TTS_URL.format(voice_id=voice_id)
    headers = {
        "xi-api-key": api_key,
        "Content-Type": "application/json",
    }
    # closing: stability高め + speed控えめでノイズ軽減
    is_closing = role == "closing"
    stability = 0.65 if is_closing else 0.4
    speed = 1.05 if is_closing else 1.15
    payload = {
        "text": text,
        "model_id": ELEVENLABS_MODEL,
        "language_code": "ja",
        "apply_text_normalization": "on",
        "voice_settings": {
            "stability": stability,
            "similarity_boost": 0.75,
            "style": 0.15 if is_closing else 0.2,
            "use_speaker_boost": True,
            "speed": speed,
        },
    }
    # 前シーンのコンテキストを渡して連続性を改善
    if previous_text:
        payload["previous_text"] = previous_text
    if previous_request_ids:
        payload["previous_request_ids"] = previous_request_ids

    try:
        resp = requests.post(url, headers=headers, json=payload, timeout=30)
        if resp.status_code != 200:
            _save_debug(
                f"elevenlabs_error_{output_path.stem}.json",
                json.dumps({"status": resp.status_code, "body": resp.text}, ensure_ascii=False, indent=2),
            )
            print(f"    ElevenLabs API エラー: {resp.status_code}")
            return False, ""

        output_path.write_bytes(resp.content)
        # レスポンスヘッダーから request_id を取得
        request_id = resp.headers.get("request-id", "")
        return True, request_id

    except Exception as e:
        print(f"    リクエストエラー: {e}")
        return False, ""


def _trim_leading_silence(audio_path: pathlib.Path) -> bool:
    """音声ファイルの先頭の無音（-40dB以下）を除去する。

    ElevenLabs の音声は冒頭に 0.1〜0.3秒の無音が入ることがあり、
    Shorts の「冒頭1秒で掴む」を阻害する。FFmpeg の silenceremove で除去する。
    """
    tmp_path = audio_path.with_suffix(".trimmed.mp3")
    try:
        result = subprocess.run(
            [
                "ffmpeg", "-y",
                "-i", str(audio_path),
                "-af", "silenceremove=start_periods=1:start_duration=0:start_threshold=-40dB",
                "-c:a", "libmp3lame", "-q:a", "2",
                str(tmp_path),
            ],
            capture_output=True,
            text=True,
            timeout=15,
        )
        if result.returncode != 0:
            return False

        # トリミング前後のサイズ差が小さすぎる場合（無音がなかった）はスキップ
        orig_size = audio_path.stat().st_size
        trim_size = tmp_path.stat().st_size
        if trim_size >= orig_size:
            tmp_path.unlink(missing_ok=True)
            return False

        # トリミング済みファイルで上書き
        tmp_path.replace(audio_path)
        return True
    except Exception:
        tmp_path.unlink(missing_ok=True)
        return False


def _get_audio_duration(audio_path: pathlib.Path) -> float:
    """FFprobe で音声の実際の長さ（秒）を取得する。"""
    try:
        result = subprocess.run(
            [
                "ffprobe", "-v", "quiet",
                "-show_entries", "format=duration",
                "-of", "default=noprint_wrappers=1:nokey=1",
                str(audio_path),
            ],
            capture_output=True,
            text=True,
            timeout=10,
        )
        return float(result.stdout.strip())
    except Exception:
        return 0.0


def _save_debug(filename: str, content: str):
    """デバッグ情報を debug/ に保存する。"""
    debug_dir = pathlib.Path(__file__).parent / "debug"
    debug_dir.mkdir(exist_ok=True)
    try:
        (debug_dir / filename).write_text(content, encoding="utf-8")
    except Exception:
        pass
